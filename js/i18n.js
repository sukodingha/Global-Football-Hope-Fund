// Current language (default: en)
let currentLang = localStorage.getItem("GFHF_LANG") || "en";

/**
 * Translate a key into the current language.
 * Falls back to English if key not found.
 */
export function __(key) {
  const langData = TRANSLATIONS[currentLang] || TRANSLATIONS.en;
  return langData[key] || TRANSLATIONS.en[key] || key;
}

/**
 * Set the active language, save preference, and re-render UI.
 */
export function setLanguage(lang) {
  if (!TRANSLATIONS[lang]) return;
  currentLang = lang;
  localStorage.setItem("GFHF_LANG", lang);
  applyTranslations();
  dispatchEvent(new CustomEvent("languageChanged", { detail: { lang } }));
}

/**
 * Get current language code.
 */
export function getLanguage() {
  return currentLang;
}

/**
 * Get all supported languages.
 */
export function getLanguages() {
  return Object.keys(TRANSLATIONS);
}

/**
 * Apply translations to all elements with data-i18n attribute.
 */
export function applyTranslations() {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const translated = __(key);
    if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
      if (el.getAttribute("type") === "submit" || el.tagName === "BUTTON") {
        el.value = translated;
      } else {
        el.placeholder = translated;
      }
    } else {
      el.textContent = translated;
    }
  });

  // Update HTML lang attribute
  document.documentElement.lang = currentLang;
}

// Auto-apply translations on DOMContentLoaded
document.addEventListener("DOMContentLoaded", () => {
  applyTranslations();
});

